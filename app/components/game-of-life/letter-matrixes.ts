export const getMatrixForLetter = (letter: string) => {
    switch(letter) {
        case 'M':
            return upperMMatrix;
        case 'S':
            return upperSMatrix;
        case 'P':
            return upperPMatrix;
        case 'i':
            return lowerIMatrix;
        case 'm':
            return lowerMMatrix;
        case 'o':
            return lowerOMatrix;
        case 'n':
            return lowerNMatrix;
        case 'e':
            return lowerEMatrix;
        case 'a':
            return lowerAMatrix;
        case 'r':
            return lowerRMatrix;
        case 'd':
            return lowerDMatrix;
        case 'y':
            return lowerYLMatrix;
        case 'p':
            return lowerPMatrix;
        case 't':
            return lowerTMatrix;
        case 'f':
            return lowerFMatrix;
        case 'l':
            return lowerLMatrix;
        case '-':
            return hyphenMatrix;
        case ' ':
            return spaceMatrix
        default:
            console.error(`Unsupported letter "${letter}" passed to getMatrixForLetter !`)
            return spaceMatrix;
    }
}